package org.Day2;

public class SwapingOfTwoNumberUsingThirdVariable {

	public static void main(String[] args) {
				
		int a1=10, b1=20;
		int temp=0;
		System.out.println("A="+a1);
		System.out.println("B="+b1);
		
		temp=a1; //  temp=10
		a1=b1; // a1=20;
		b1=temp;
		
		System.out.println("A1="+a1);
		System.out.println("B1="+b1);
		
		
		
		
	}

}
